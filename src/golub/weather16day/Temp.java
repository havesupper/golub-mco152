package golub.weather16day;

public class Temp {

	private double day;
	private double night;
	private double eve;
	private double morn;

	public double getDay() {
		return day;
	}

	public double getNight() {
		return night;
	}

	public double getEve() {
		return eve;
	}

	public double getMorn() {
		return morn;
	}

}
